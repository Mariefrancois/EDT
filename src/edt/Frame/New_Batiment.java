private void refresh(){
    this.nom.setText("");
    this.latitude.setText("0.0");
    this.longitude.setText("0.0");
    this.donner.desactive_sup_modif();
    this.batiment = null;
    this.donner.refreshBatiment();
}
private void creer(){
        this.batiment.setNom(this.nom.getText());
        this.batiment.setLat(Double.parseDouble(this.latitude.getText()));
        this.batiment.setLon(Double.parseDouble(this.longitude.getText()));
}
    private void validerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validerActionPerformed
        // TODO add your handling code here:
        BD_MySQL.init();
        switch(etat){
            case Batiment:
                this.batiment = new Batiment(this.nom.getText(),Double.parseDouble(this.latitude.getText()),Double.parseDouble(this.longitude.getText()));
                this.batiment.save();
                refresh();
                etat = Etat.Batiment;
                break;
            case Batiment1:
                creer();
                this.batiment.save();
                refresh();
                this.dispose();
                break;
                
        }
    }//GEN-LAST:event_validerActionPerformed

    private void annulerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_annulerActionPerformed
        // TODO add your handling code here:
        switch(etat){
            case Batiment:
                this.dispose();
                this.donner.desactive_sup_modif();
                break;
            case Batiment1:
                this.dispose();
                this.donner.desactive_sup_modif();
                break;
                
        }
    }//GEN-LAST:event_annulerActionPerformed

