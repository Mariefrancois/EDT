    private enum Etat{
        Batiment,
        Batiment1
    }
    private Etat etat;
    public void init(String n,int id){
        if(n.equals("Batiment"))
            etat = Etat.Batiment;
        else if (n.equals("Batiment1")){
            etat = Etat.Batiment1;
            try {
                this.batiment = new Batiment(id);
            } catch (SQLException ex) {
                Logger.getLogger(New_Etudiant.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.nom.setText(batiment.getNom());
            this.latitude.setText(String.valueOf(batiment.getLat()));
            this.longitude.setText(String.valueOf(batiment.getLon()));
        }
    }

